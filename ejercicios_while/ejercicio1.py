# 1. Leer números enteros de teclado, hasta que el usuario ingrese el 0.
# Finalmente, mostrar la sumatoria de todos los números ingresados.

# Variables
# Proceso
# Salida

numero = 1 # Bandera 0 F - 1 V
suma = 0 # Contador

while numero != 0:
    numero = int(input("Ingrese otro número cualquiera. Para salir ingrese 0: "))
    suma += numero

print("La suma es ", suma)



    








